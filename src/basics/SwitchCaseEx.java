package basics;

public class SwitchCaseEx {
    public static void main(String[] args) {
        int a = 8 ;
        switch (a){
            case  1 :
                System.out.println("Your num is one ");
                break;
            case  2 :
                System.out.println("Your num is two ");
                break;
            case  3 :
                System.out.println("Your num is three ");
                break;
            default:
                System.out.println("your num is wrong");

        }
    }
}
