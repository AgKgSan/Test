package basics;

public class ControlFlow2 {
    public static void main(String[] args) {
        String day = "Monday";
        switch (day){
            case "Monday":
                System.out.println("This day is Monday");
                System.out.println("I have to go somewhere");
                break;

            case "Tuesday":
                System.out.println("This day is tuesday");
                break;
            case  "Wednesday":
                System.out.println("this day is wednesday");
                break;
            default:

        }
    }
}
