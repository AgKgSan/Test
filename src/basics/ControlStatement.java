package basics;

public class ControlStatement {
    public static void main(String[] args) {
        String day = "monday";
        if (day=="monday"){
            System.out.println("go to school");
        }else if (day=="tuesday"){
            System.out.println("no go to school");
        }else if (day=="wednesday"){
            System.out.println("no go to school");
        }else if (day=="tuesday"){
            System.out.println("no go to school");
        }else {
            System.out.println("blah blah blah");
        }
    }

}
