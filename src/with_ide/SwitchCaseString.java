package with_ide;

public class SwitchCaseString {
    public static void main(String[] args) {
        String i = "seven" ;
        switch (i) {
            case "one":
                System.out.println("The num is one ");
                break;
            case "two":
                System.out.println("The num is two ");
                break;
            case "three":
                System.out.println("The num is three");
                break;
            case "four":
                System.out.println("The num is four");
                break;
            case "five":
                System.out.println("The num is five ");
                break;
            case "six":
                System.out.println("The num is six ");
                break;
            case "seven":
                System.out.println("The num is seven");
                break;
        }
    }
}
